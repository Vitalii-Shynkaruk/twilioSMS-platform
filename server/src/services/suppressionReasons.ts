export const SUPPRESSION_REASONS = {
  AUTO_BAD_NUMBER: 'AUTO_BAD_NUMBER',
  INVALID_DESTINATION: 'INVALID_DESTINATION',
  NON_MOBILE_DESTINATION: 'NON_MOBILE_DESTINATION',
  LOOKUP_INVALID: 'LOOKUP_INVALID',
  LOOKUP_QUARANTINE: 'LOOKUP_QUARANTINE',
  LOOKUP_RETRY_PENDING: 'LOOKUP_RETRY_PENDING',
  LOOKUP_FAILED_REVIEW: 'LOOKUP_FAILED_REVIEW',
  QUARANTINE_TRANSIENT: 'QUARANTINE_TRANSIENT',
  STOP: 'STOP',
  DNC: 'DNC',
  NOT_INTERESTED: 'NOT_INTERESTED',
} as const;

export type SuppressionReason = (typeof SUPPRESSION_REASONS)[keyof typeof SUPPRESSION_REASONS];

export const AUTO_GENERATED_SUPPRESSION_REASONS = new Set<string>([
  SUPPRESSION_REASONS.AUTO_BAD_NUMBER,
  SUPPRESSION_REASONS.INVALID_DESTINATION,
  SUPPRESSION_REASONS.NON_MOBILE_DESTINATION,
  SUPPRESSION_REASONS.LOOKUP_INVALID,
  SUPPRESSION_REASONS.LOOKUP_QUARANTINE,
  SUPPRESSION_REASONS.LOOKUP_RETRY_PENDING,
  SUPPRESSION_REASONS.LOOKUP_FAILED_REVIEW,
  SUPPRESSION_REASONS.QUARANTINE_TRANSIENT,
]);

export const LOOKUP_SUPPRESSION_REASONS = new Set<string>([
  SUPPRESSION_REASONS.LOOKUP_INVALID,
  SUPPRESSION_REASONS.LOOKUP_QUARANTINE,
  SUPPRESSION_REASONS.LOOKUP_RETRY_PENDING,
  SUPPRESSION_REASONS.LOOKUP_FAILED_REVIEW,
]);

export const DELIVERY_SUPPRESSION_REASONS = new Set<string>([
  SUPPRESSION_REASONS.AUTO_BAD_NUMBER,
  SUPPRESSION_REASONS.INVALID_DESTINATION,
  SUPPRESSION_REASONS.NON_MOBILE_DESTINATION,
  SUPPRESSION_REASONS.QUARANTINE_TRANSIENT,
]);

export const LOCKED_SUPPRESSION_REASONS = new Set<string>([
  SUPPRESSION_REASONS.STOP,
  SUPPRESSION_REASONS.DNC,
  SUPPRESSION_REASONS.NOT_INTERESTED,
]);

export function isAutoGeneratedSuppressionReason(reason?: string | null): boolean {
  return !!reason && AUTO_GENERATED_SUPPRESSION_REASONS.has(reason);
}

export function isLockedSuppressionReason(reason?: string | null): boolean {
  return !!reason && LOCKED_SUPPRESSION_REASONS.has(reason);
}

export function isLookupSuppressionReason(reason?: string | null): boolean {
  return !!reason && LOOKUP_SUPPRESSION_REASONS.has(reason);
}

export function isDeliverySuppressionReason(reason?: string | null): boolean {
  return !!reason && DELIVERY_SUPPRESSION_REASONS.has(reason);
}
